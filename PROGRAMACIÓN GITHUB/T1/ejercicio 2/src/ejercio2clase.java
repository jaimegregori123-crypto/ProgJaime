public class ejercio2clase {

    public static void main(String[] args){
        //definir la variable puntuacion con valor inicial 0
        int puntuacion = 0;
        System.out.println("Puntuación inicial: " + puntuacion);

        //primera modificación
        puntuacion +=5;
        System.out.println("Despues de la primera modificación: " + puntuacion);

        //segunda modificación
        puntuacion +=5;
        System.out.println("Despues de la segunda modificación: " + puntuacion);

        //tercera modificación
        puntuacion +=5;
        System.out.println("Puntuación final: " + puntuacion);





    }
}
